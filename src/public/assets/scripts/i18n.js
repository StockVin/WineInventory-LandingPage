(function () {
  const defaultLang = document.documentElement.getAttribute('data-lang') || 'en';
  let currentLang = defaultLang;

  function translateElement(el, key, lang, useHtml = false) {
    const dict = translations[lang] || {};
    const value = dict[key] ?? '';
    if (useHtml) {
      el.innerHTML = value;
    } else {
      el.textContent = value;
    }
  }

  function applyTranslations(lang) {
    currentLang = lang;

    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (!key) return;
      translateElement(el, key, lang, false);
    });
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      const key = el.getAttribute('data-i18n-html');
      if (!key) return;
      translateElement(el, key, lang, true);
    });
    document.querySelectorAll('meta[data-i18n-html]').forEach(meta => {
      const key = meta.getAttribute('data-i18n-html');
      const dict = translations[lang] || {};
      const value = dict[key] ?? '';
      if (meta.name) meta.content = value;
    });
    const titleKey = 'document.title';
    if (translations[lang] && translations[lang][titleKey]) {
      document.title = translations[lang][titleKey];
    }
  }
  document.addEventListener('DOMContentLoaded', () => {
    applyTranslations(currentLang);
  });
  document.querySelectorAll('[data-lang]').forEach(btn => {
    btn.addEventListener('click', (e) => {
      const lang = btn.getAttribute('data-lang');
      if (!lang || !translations[lang]) return;
      document.documentElement.setAttribute('data-lang', lang);
      document.querySelectorAll('[data-lang]').forEach(b => {
        b.setAttribute('aria-pressed', b === btn ? 'true' : 'false');
      });
      applyTranslations(lang);
    });
  });
  window.__applyTranslations = applyTranslations;
  window.__currentLang = () => currentLang;
})();
function setLanguage(lang) {
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const translation = translations[lang][key];
    if (translation) {
      el.textContent = translation;
    }
  });
}

